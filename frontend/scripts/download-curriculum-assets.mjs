#!/usr/bin/env node
/**
 * Downloads HTTPS image URLs referenced by mock/import JSON into assets/curriculum/
 * and writes assetMap.generated.ts with static require() entries for Metro.
 *
 * Usage: node scripts/download-curriculum-assets.mjs
 * Re-run when curriculum URLs change.
 */

import crypto from 'crypto';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FRONTEND = path.join(__dirname, '..');
const OUT_DIR = path.join(FRONTEND, 'assets', 'curriculum');
const MAP_FILE = path.join(OUT_DIR, 'assetMap.generated.ts');

/** Scan these trees for https:// image URLs to mirror (keep in sync with active curriculum). */
const JSON_ROOTS = [path.join(FRONTEND, 'mock')];

const MARKDOWN_IMG = /!\[[^\]]*\]\((https?:\/\/[^)\s]+)\)/g;

const UA =
  'MedicalStudyHub/1.0 (educational app; local curriculum asset mirror; https://github.com/)';

function walkJsonFiles(dir, acc) {
  if (!fs.existsSync(dir)) return acc;
  for (const name of fs.readdirSync(dir)) {
    const p = path.join(dir, name);
    const st = fs.statSync(p);
    if (st.isDirectory()) walkJsonFiles(p, acc);
    else if (name.endsWith('.json')) acc.push(p);
  }
  return acc;
}

function collectUrlsFromValue(value, set) {
  if (typeof value === 'string') {
    if (/^https?:\/\//i.test(value)) set.add(value.trim());
    let m;
    const re = new RegExp(MARKDOWN_IMG.source, 'g');
    while ((m = re.exec(value))) set.add(m[1].trim());
    return;
  }
  if (Array.isArray(value)) {
    value.forEach((v) => collectUrlsFromValue(v, set));
    return;
  }
  if (value && typeof value === 'object') {
    Object.values(value).forEach((v) => collectUrlsFromValue(v, set));
  }
}

function urlToSafeFilename(url) {
  const u = new URL(url);
  const last = u.pathname.split('/').filter(Boolean).pop() || 'asset';
  const ext = path.extname(last) || '.png';
  const base = path.basename(last, ext).replace(/[^a-zA-Z0-9._-]+/g, '_').slice(0, 64);
  const hash = crypto.createHash('sha256').update(url).digest('hex').slice(0, 10);
  return `${hash}_${base}${ext}`;
}

async function downloadUrl(url, destPath) {
  const res = await fetch(url, {
    redirect: 'follow',
    headers: {
      'User-Agent': UA,
      Accept: 'image/*,*/*;q=0.8',
    },
  });
  if (!res.ok) {
    throw new Error(`HTTP ${res.status} for ${url}`);
  }
  const buf = Buffer.from(await res.arrayBuffer());
  if (buf.length < 64) {
    throw new Error(`Suspiciously small response (${buf.length}b) for ${url}`);
  }
  fs.writeFileSync(destPath, buf);
  const ct = res.headers.get('content-type') || '';
  return { bytes: buf.length, contentType: ct };
}

function writeMap(entries) {
  const lines = entries
    .sort((a, b) => a.url.localeCompare(b.url))
    .map(({ url, file }) => {
      const rel = './' + file.replace(/\\/g, '/');
      const key = JSON.stringify(url);
      return `  ${key}: require(${JSON.stringify(rel)}),`;
    });

  const body = `/* eslint-disable @typescript-eslint/no-require-imports */
/**
 * AUTO-GENERATED by scripts/download-curriculum-assets.mjs — do not edit by hand.
 * Maps original curriculum HTTPS URLs to bundled static assets for offline display.
 */

export const CURRICULUM_ASSET_MAP: Record<string, number> = {
${lines.join('\n')}
};
`;

  fs.writeFileSync(MAP_FILE, body, 'utf8');
}

async function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });

  const jsonFiles = [];
  for (const root of JSON_ROOTS) walkJsonFiles(root, jsonFiles);

  const urlSet = new Set();
  for (const file of jsonFiles) {
    const raw = fs.readFileSync(file, 'utf8');
    let data;
    try {
      data = JSON.parse(raw);
    } catch {
      continue;
    }
    collectUrlsFromValue(data, urlSet);
  }

  const urls = [...urlSet].filter((u) => {
    try {
      const parsed = new URL(u);
      if (parsed.hostname === 'example.com' || parsed.hostname === 'localhost') return false;
      const p = parsed.pathname.toLowerCase();
      return /\.(png|jpe?g|gif|webp|svg)(\?|$)/i.test(p) || /upload\.wikimedia\.org/i.test(u);
    } catch {
      return false;
    }
  });

  console.log(`Found ${urls.length} image URLs across ${jsonFiles.length} JSON files.`);

  const entries = [];
  for (const url of urls) {
    const filename = urlToSafeFilename(url);
    const destPath = path.join(OUT_DIR, filename);
    if (!fs.existsSync(destPath)) {
      process.stdout.write(`Fetching ${url.slice(0, 80)}… `);
      try {
        const { bytes } = await downloadUrl(url, destPath);
        console.log(`ok (${bytes} bytes) → ${filename}`);
      } catch (e) {
        console.log(`FAILED: ${e.message}`);
        continue;
      }
    } else {
      console.log(`Skip (exists): ${filename}`);
    }
    entries.push({ url, file: filename });
  }

  if (entries.length === 0) {
    console.warn('No assets downloaded. Writing empty map.');
    fs.writeFileSync(
      MAP_FILE,
      `/* eslint-disable @typescript-eslint/no-require-imports */
/** Empty map — run: node scripts/download-curriculum-assets.mjs */
export const CURRICULUM_ASSET_MAP: Record<string, number> = {};
`,
      'utf8',
    );
    return;
  }

  writeMap(entries);
  console.log(`Wrote ${MAP_FILE} (${entries.length} entries).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
